    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Funcion de donde se busca los registros y buscar si esta registrado o no
        try {
            Connection cn;
            cn = DriverManager.getConnection("jdbc:mysql://localhost/final", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from existencia where ID = ?");
            pst.setString(1, txt_buscar.getText().trim());
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {

                txt_1.setText(rs.getString("codigo_bodega"));
                txt_2.setText(rs.getString("codigo_producto"));
                txt_3.setText(rs.getString("saldo_existencia"));

            } else {
                JOptionPane.showMessageDialog(null, " no registrado.");
            }

        } catch (Exception e) {

        }
    }//GEN-LAST:event_jButton2ActionPerformed

